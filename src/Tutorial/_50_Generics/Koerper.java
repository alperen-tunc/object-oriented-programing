package Tutorial._50_Generics;

public class Koerper<A, B>
{
    private A a;
    private B b;

    public A getA()
    {
        return a;
    }

    public void setA(A a)
    {
        this.a = a;
    }

    public B getB()
    {
        return b;
    }

    public void setB(B b)
    {
        this.b = b;
    }

}
